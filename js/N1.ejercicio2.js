/*Operaciones matemáticas: Crea un programa que sume, reste, multiplique y
divida dos números y muestre el resultado en la consola.*/

let numero1 = parseFloat(prompt("Ingresa el primer valor: "));
let numero2 = parseFloat(prompt("Ingresa el segunda valor: "));

let suma = numero1 + numero2;
console.log("La suma es: " + suma);

let resta = numero1 - numero2;
console.log("La resta es: " + resta);

let multiplicacion = numero1 * numero2;
console.log("La multiplicacion es: " + multiplicacion);

let division = numero1 / numero2;
console.log("La division es: " + division);